																												<p><b>Mumbai, India, February, xx 2021</b><span style="font-weight: 400;">: TresVista, a leading provider of high-end outsourced support to corporates, asset managers, and entrepreneurs, has </span><b>announced a one-time bonus of INR 50,000</b><span style="font-weight: 400;"> to every Analyst and Associate (equivalent), across review cycles, as a gesture of thanks for outstanding performance despite the 2020 pamdemic. Tresvista will also undertake a firmwide pay revision exercise by July 2021, and plans to invest heavily in training and technology. At the current CAGR of ~30%, TresVista expects to have a strength of ~2,500 people in four years.</span></p>
				
																			<p><b>Sudeep Mishra</b><span style="font-weight: 400;">, </span><b>Managing Director and Co-Founder TresVista</b><span style="font-weight: 400;">, commented on the announcement, &#8220;</span><i><span style="font-weight: 400;">TresVista has always been  an employee-centric organization and we strive to constantly do better. The bonus is a way of us thanking our employees who have stepped up despite the challenging circumstances caused by the pandemic. Our aim is for TresVista’s impact to be far wider and deeper, and be associated with excellence, thereby enhancing opportunities for all employees who have spent years at the firm.</span></i><span style="font-weight: 400;">”</span></p>
				
																			<p><span style="font-weight: 400;">In 2020, Tresvista hired close to 300 employees for its delivery centres in Mumbai, Pune and Bangalore. It also introduced a slew of employee-friendly measures to support employees during COVID-19 like Caregivers’ Special Leave, COVID+ve paid leaves, and WFH setup reimbursements.</span></p>
				
																			<h3>About TresVista</h3>
<p>Founded in 2006, TresVista has been the leading provider of high-end outsourced support for asset managers, entrepreneurs &amp; corporates for over a decade. TresVista’s support functions include business plan development, all aspects of fund administration, portfolio management, valuation &amp; research, and deal sourcing &amp; execution. With over 700 employees across offices in Bengaluru, Mumbai, New York, Pune, and Singapore, TresVista delivers cost and operational efficiencies for over 1,000 clients across geographies and asset classes, who collectively manage over $10 trillion in AUM.</p>
				
