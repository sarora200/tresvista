							<div class="ind_blog_title">
								<h2>How did we help a Family Office get better insight into SPACs in the healthcare sector in the US?</h2>
							</div>
						</div>
																												<h3></h3>
<h3><span style="font-weight: 400;">The Context</span></h3>
<p><span style="font-weight: 400;">The client, Family Office investing in the healthcare space in the United States, keeps a close track on recent developments and upcoming trends to analyze new opportunities and to understand the impact of these developments on their portfolio. The task at hand was to get an overview of the SPAC landscape and analyze de-SPAC transactions in the health care sector over the past three years. </span></p>
<p>&nbsp;</p>
<p><span style="font-weight: 400;">The client asked for a detailed summary of the SPAC transactions in healthcare sector which included transaction details, target company description, management details of the SPAC and target company, and an analysis of the prospectus.</span></p>
<p>&nbsp;</p>
				
																			<h3></h3>
<h3><span style="font-weight: 400;">Our Objective</span></h3>
<p><span style="font-weight: 400;">To prepare a research report on SPACs to showcase its current landscape and future trends. </span></p>
<p><span style="font-weight: 400;">The main pointers were: </span></p>
<ul class="ind_blog_ul">
<li style="font-weight: 400;" aria-level="1"><span style="font-weight: 400;">Capture SPAC activity of the last decade (through number of SPAC IPO’s and money raised)</span></li>
<li style="font-weight: 400;" aria-level="1"><span style="font-weight: 400;">Analyze the sector focus of de-SPAC transactions in 2020 (Amount of money poured into various sectors via SPACs)</span></li>
<li style="font-weight: 400;" aria-level="1"><span style="font-weight: 400;">Analyze the reasons for recent boom in the SPAC scenario</span></li>
<li style="font-weight: 400;" aria-level="1"><span style="font-weight: 400;">Summarise de-SPAC transactions in the health care space</span></li>
<li style="font-weight: 400;" aria-level="1"><span style="font-weight: 400;">Update details of the current outstanding SPACs, to analyze the companies which are “on the clock” to complete a de-SPAC transaction</span></li>
</ul>
				
																			<h3></h3>
<h3><span style="font-weight: 400;">How did we go about it?</span></h3>
<p><span style="font-weight: 400;">The project request was broken down into smaller tasks to maintain efficiency at each step. Firstly, it was very important to understand the working of the SPACs, before we could get down and drill through the different asks of the request. Once we were able to digest this information, we then moved to gather publicly available information through a combination of desktop research and third-party databases. Additionally, the most relevant pieces of information were captured from press releases, articles, reports published by research agencies and opinion columns. After verifying the relevance of the data with the client, the team sought to compile the industry metrics mentioned by the client and present it in a format that was concise and orderly.</span></p>
<p><span style="font-weight: 400;">Here are the specific steps we followed to ensure we delivered on the request and more:</span></p>
<p><span style="font-weight: 400;">We analyzed the requirements for this task and laid out a strategy to tackle every possible hurdle that we could face while researching.</span></p>
<p><span style="font-weight: 400;">Then, we gathered the publicly available data and tried to sync various data pieces to create a broad picture of SPACs. We also Leveraged the third-party databases (Thomson Eikon and Factset) to gather information regarding M&amp;A deals.</span></p>
<p><span style="font-weight: 400;">We captured the money flowing into each sector from SPACs. Information regarding the industry was gathered primarily from desktop search using press articles, reports published by research agencies and opinion columns.</span></p>
<p><span style="font-weight: 400;">We then summarized SPAC transactions in the healthcare sector. The team was subsequently engaged to periodically update the transaction details and other statistics.</span></p>
				
																			<h3></h3>
<h3><span style="font-weight: 400;">The challenges we faced and how we overcame them</span></h3>
<p><span style="font-weight: 400;">Due to loads of unauthenticated data on the public sources, it was challenging to verify them and then use it for the broader purpose.</span></p>
<p><span style="font-weight: 400;">We tackled these hurdles by carefully understanding the working of a SPAC through articles, posts, blogs, videos, etc; and then verified each industry metric with multiple sources along with company the board presentations.</span></p>
				
																			<h3></h3>
<h3><span style="font-weight: 400;">Key Takeaways</span></h3>
<ul class="ind_blog_ul">
<li>The current market scenario is favourable for private companies to go public because of increasing public markets valuations</li>
<li>SPACs are a relatively easy medium of getting a private company listed at the exchange and there is an increasing volume of de-SPAC transactions since 2020</li>
<li>The valuations of private companies getting listed through SPACs are on the higher side in comparison to the traditional IPO process</li>
</ul>
				
